function pesquisar() {
    var pesquisa = document.getElementById("pesquisa").value;
  
    var produtosFiltrados = livrosObj.filter(produto => {
      return produto.titulo.toLowerCase().includes(pesquisa.toLowerCase()) ||
        produto.categoria.toLowerCase().includes(pesquisa.toLowerCase());
    });
    if (!pesquisa) {
      produtosFiltrados = livrosObj;
    }
    var html = "";
    produtosFiltrados.forEach(produto => {
      html +=
        `<div class="produto">
        <h3>${produto.titulo}</h3>
        <img src="${produto.imgCapa}" />
        <div class="botao">
            <a href="${produto.arquivo}" download>
                Baixe aqui
            </a>
        </div>
    </div>`;
    });

    
  
    document.querySelector("section#livros.produtos").innerHTML = html;
  }
  
  document.getElementById("pesquisa").addEventListener("input", pesquisar);
  
  pesquisar();

